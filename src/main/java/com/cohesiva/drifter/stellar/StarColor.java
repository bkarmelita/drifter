package com.cohesiva.drifter.stellar;

/**
 * The <code>StarColor</code> represents a star color enumeration. The
 * <code>StarColor</code> reflects how the star is being displayed.
 * 
 * @author carmel
 */
public enum StarColor {

	/**
	 * A blue star color.
	 */
	BLUE,

	/**
	 * A white/blue star color.
	 */
	BLUE_WHITE,

	/**
	 * A white star color.
	 */
	WHITE,

	/**
	 * A yellow/white star color.
	 */
	YELLOW_WHITE,

	/**
	 * A yellow star color.
	 */
	YELLOW,

	/**
	 * An orange star color.
	 */
	ORANGE,

	/**
	 * A red star color.
	 */
	RED;

}
